export function calculate(year: number, month: number, day: number): number | null {
  const ymData = yearMonthTable[year];
  if (!ymData) return null;
  const monthValue = ymData[month - 1];
  let result = monthValue + day;
  if (result > 60) result -= 60;
  if (result === 0) result = 60;
  return result;
}

